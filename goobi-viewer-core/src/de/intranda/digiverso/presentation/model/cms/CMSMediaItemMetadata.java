package de.intranda.digiverso.presentation.model.cms;

import javax.persistence.Column;
import javax.persistence.Embeddable;

@Embeddable
public class CMSMediaItemMetadata {

    @Column(name = "language", nullable = false)
    private String language;

    @Column(name = "name")
    private String name;

    @Column(name = "description")
    private String description;

    /**
     * default constructor
     */
    public CMSMediaItemMetadata() {
        
    }
    
    /**
     * copy constructor
     * @param origMetadata
     */
    public CMSMediaItemMetadata(CMSMediaItemMetadata orig) {
        this.language = orig.language;
        this.name = orig.name;
        this.description = orig.description;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((language == null) ? 0 : language.hashCode());
        //        result = prime * result + ((ownerItem == null) ? 0 : ownerItem.hashCode());
        return result;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        CMSMediaItemMetadata other = (CMSMediaItemMetadata) obj;
        if (language == null) {
            if (other.language != null) {
                return false;
            }
        } else if (!language.equals(other.language)) {
            return false;
        }
        return true;
    }

    /**
     * @return the language
     */
    public String getLanguage() {
        return language;
    }

    /**
     * @param language the language to set
     */
    public void setLanguage(String language) {
        this.language = language;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

}
