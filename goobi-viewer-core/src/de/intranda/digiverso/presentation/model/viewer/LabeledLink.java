package de.intranda.digiverso.presentation.model.viewer;

import java.io.Serializable;

import org.apache.commons.lang.StringUtils;

public class LabeledLink implements Serializable {

    private static final long serialVersionUID = -2546718627110716169L;

    private String name;
    private String url;
    private int weight;

    public LabeledLink(String name, String url, int weight) {
        this.name = name;
        this.url = url;
        this.weight = weight;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * @return the weight
     */
    public int getWeight() {
        return weight;
    }

    /**
     * @param weight the weight to set
     */
    public void setWeight(int weight) {
        this.weight = weight;
    }
    
    public boolean isLink() {
        return StringUtils.isNotBlank(getUrl());
    }


    @Override
    public String toString() {
        return name + " : " + url + " : " + weight;
    }
}
